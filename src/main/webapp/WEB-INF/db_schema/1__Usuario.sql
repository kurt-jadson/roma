CREATE TABLE SYS_USUARIO (
	ID BIGINT NOT NULL PRIMARY KEY AUTO_INCREMENT,
	USERNAME VARCHAR(255) NOT NULL,
	PASSWORD VARCHAR(255) DEFAULT NULL
);

CREATE TABLE SYS_ROLE (
	ID BIGINT NOT NULL PRIMARY KEY AUTO_INCREMENT,
	USERNAME VARCHAR(255) NOT NULL,
	ROLENAME VARCHAR(255) NOT NULL
);

